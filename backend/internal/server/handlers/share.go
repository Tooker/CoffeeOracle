// renderInline escapes HTML and applies basic inline markdown like bold.
func renderInline(text string) string {
	escaped := html.EscapeString(text)
	return boldPattern.ReplaceAllString(escaped, "<strong>$1</strong>")
}

// markdownToHTML renders the subset of Markdown generated by this app.
func markdownToHTML(markdown string) string {
	var out strings.Builder
	inList := false

	closeList := func() {
		if inList {
			out.WriteString("</ul>")
			inList = false
		}
	}

	for _, line := range strings.Split(markdown, "\n") {
		trimmed := strings.TrimSpace(line)

		if trimmed == "" {
			closeList()
			continue
		}

		if strings.HasPrefix(trimmed, "!") {
			closeList()
			start := strings.Index(trimmed, "](")
			if start > 0 && strings.HasSuffix(trimmed, ")") {
				alt := strings.TrimPrefix(trimmed[:start], "![")
				src := strings.TrimSuffix(trimmed[start+2:], ")")
				out.WriteString(`<img src="` + html.EscapeString(src) + `" alt="` + html.EscapeString(alt) + `" />`)
			}
			continue
		}
		if strings.HasPrefix(trimmed, "# ") {
			closeList()
			out.WriteString("<h1>" + renderInline(strings.TrimPrefix(trimmed, "# ")) + "</h1>")
			continue
		}
		if strings.HasPrefix(trimmed, "## ") {
			closeList()
			out.WriteString("<h2>" + renderInline(strings.TrimPrefix(trimmed, "## ")) + "</h2>")
			continue
		}
		if strings.HasPrefix(trimmed, "### ") {
			closeList()
			out.WriteString("<h3>" + renderInline(strings.TrimPrefix(trimmed, "### ")) + "</h3>")
			continue
		}
		if strings.HasPrefix(trimmed, "#### ") {
			closeList()
			out.WriteString("<h4>" + renderInline(strings.TrimPrefix(trimmed, "#### ")) + "</h4>")
			continue
		}
		if strings.HasPrefix(trimmed, "- ") || strings.HasPrefix(trimmed, "* ") {
			if !inList {
				out.WriteString("<ul>")
				inList = true
			}
			out.WriteString("<li>" + renderInline(trimmed[2:]) + "</li>")
			continue
		}
		if strings.HasPrefix(trimmed, "> ") {
			closeList()
			out.WriteString("<blockquote>" + renderInline(strings.TrimPrefix(trimmed, "> ")) + "</blockquote>")
			continue
		}

		closeList()
		out.WriteString("<p>" + renderInline(trimmed) + "</p>")
	}
	closeList()
	return out.String()
}
