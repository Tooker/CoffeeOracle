var shareIDPattern = regexp.MustCompile(`^[a-f0-9-]{36}$`)
var boldPattern = regexp.MustCompile(`\*\*(.*?)\*\*`)

// NewShareHandler serves permanent oracle readings and their raw markdown files.
func NewShareHandler() http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if r.Method != http.MethodGet {
			http.Error(w, http.StatusText(http.StatusMethodNotAllowed), http.StatusMethodNotAllowed)
			return
		}

		name := path.Base(strings.TrimPrefix(r.URL.Path, "/api/share/"))
		wantsMarkdown := strings.HasSuffix(name, ".md")
		id := strings.TrimSuffix(name, ".md")
		if !shareIDPattern.MatchString(id) {
			http.NotFound(w, r)
			return
		}

		markdown, err := os.ReadFile(filepath.Join(readingStoreDir, id+".md"))
		if err != nil {
			http.NotFound(w, r)
			return
		}

		if wantsMarkdown {
			w.Header().Set("Content-Type", "text/markdown; charset=utf-8")
			_, _ = w.Write(markdown)
			return
		}

		w.Header().Set("Content-Type", "text/html; charset=utf-8")
		_, _ = w.Write([]byte(renderShareHTML(string(markdown), r.URL.Path+".md")))
	})
}

